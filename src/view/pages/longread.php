  <h2><span class="hidden">Verhaal</span></h2>
  <p class="summary__opening">De internationaal bekroonde roman van Ray Bradbury, Fahrenheit 451, is een meesterwerk van de twintigste-eeuwse literatuur en speelt zich af in een sombere dystopische toekomst.</p>
  <p class="summary__book">Guy Montag is brandweerman. In zijn wereld, waar de televisie heerst en literatuur nagenoeg is uitgestorven, blussen brandweermannen geen vuren: ze beginnen ze juist. Het is zijn baan om de meest illegale artikelen te verwoesten: boeken. Ook de huizen waarin die boeken zijn verborgen, gaan in vlammen op. Montag twijfelt nooit aan de vernietiging en verwoesting die zijn acties teweegbrengen, en keert aan het eind van de dag altijd vrolijk terug naar zijn saaie leven en zijn vrouw, Mildred, die de hele dag met haar “familie” op televisie doorbrengt. Maar dan ontmoet hij zijn excentrieke jonge buurvrouw, Clarisse, die hem introduceert tot een verleden waar mensen niet in angst leefden en tot een heden waar men de wereld ziet door de ideeën in boeken in plaats van het hersenloze gepraat op de televisie. Als Mildred probeert zelfmoord te plegen en Clarisse verdwijnt, begint Montag overal vraagtekens bij te zetten: vooral bij wat hij nooit heeft geweten. Hij begint boeken te verbergen in zijn huis, en als zijn misdaad wordt ontdekt, moet de brandweerman vluchten voor zijn leven.</p>
  <div class="wrapper">
    <div class="summary__history">
      <p>Fahrenheit 451 is geïnspireerd door de vele boekbranding doorheen de Verenigde Staten. De titel ‘Fahrenheit 451’ was dan ook niet willekeurig gekozen, deze verwijst naar de temperatuur waar een boek in brand schiet. Ray Bradbury, de schrijver van dit boek, was ongerust door de boekbranding tijdens de WOII en de censuur van vrije ideeën. Hij zag hoe de opkomst van media de interesse in lezen verminderde en besloot op basis hiervan de internationale bestseller Fahrenheit 451 te schrijven.</p>
