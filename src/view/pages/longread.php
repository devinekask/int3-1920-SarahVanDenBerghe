      <h3 class="title">De harde realiteit</h3>
      <p>Tot diep in de 19de eeuw keurden geestelijken het lezen door het gewone volk af. Lezen zou lui maken, tijdsverspilling zijn of eenvoudige mensen op de verkeerde ideeën brengen. Vooral romans waren slecht: ze hadden een afstompende werking op het geweten en verslapten de wil. Het verbranden van boeken, geschreven teksten en mediadragers meestal publiekelijk en ritueel verbranden is iets dat doorheen de geschiedenis gebeurde. Het motief voor boekverbranding komt neer op censuur op morele, religieuze en politieke gronden. </p>
      <p>In sommige gevallen zijn de vernietigde werken onvervangbaar en vormt hun verbranding een ernstig verlies voor cultureel erfgoed. In andere gevallen, zoals de boekverbrandingen van de nazi’s , overleven kopieën van de vernietigde boeken. Het branden van boeken wordt symbolisch voor een hard en onderdrukkend regime gezien, dat een aspect van de heersende cultuur probeert te censureren of tot zwijgen te brengen. Het kan een minachting zijn voor de inhoud van het boek of de auteur, en de handeling is bedoeld om bredere publieke aandacht voor deze mening te vestigen.</p>
